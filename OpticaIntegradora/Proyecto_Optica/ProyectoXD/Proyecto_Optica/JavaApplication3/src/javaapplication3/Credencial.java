    public void insertarRegistroEnTabla(String nombre) {
        contador++;

        LocalDateTime hoy = LocalDateTime.now();

        String fecha = hoy.getDayOfMonth() + "/" + hoy.getMonthValue() + "/" + hoy.getYear();
        String hora = hoy.getHour() + ":" + hoy.getMinute();

        String[] info = new String[3];

        info[0] = String.valueOf(contador) + " " + nombre;
        info[1] = fecha;
        info[2] = hora;

        modelo.addRow(info);
    }

    public void enviarDatosArduino(Usuario currentAlumno) throws Exception {

        String messageToArduino = currentAlumno.showAlumno();

        serial.enviarDatos(messageToArduino);

    }

