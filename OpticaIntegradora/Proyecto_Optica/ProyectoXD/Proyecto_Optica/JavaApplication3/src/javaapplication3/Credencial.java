    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        try {
            Statement stmt = con.getConnection();

            String matricula = txtMatricula.getText();

            if (matricula.length() > 8) {
                JOptionPane.showMessageDialog(null, "Esto no es una matricula de credencial");
            } else {
                ResultSet res = stmt.executeQuery("SELECT * FROM Alumnos where matricula = " + matricula);

                while (res.next()) {

                    alumno.setMatricula(res.getString("matricula"));
                    alumno.setNombre(res.getString("nombre"));
                    alumno.setApellidoPaterno(res.getString("apellidoPaterno"));
                    alumno.setApellidoMaterno(res.getString("apellidoMaterno"));
                    alumno.setGrupo(res.getString("grupo"));
                    alumno.setCarrera(res.getString("carrera"));

                }

                matriculaa.setText(alumno.getMatricula());
                nombre.setText(alumno.getNombre());
                carrera.setText(alumno.getCarrera());
                jLabel8.setText(alumno.getGrupo());

                ImageIcon wallper = new ImageIcon("src/imagenes/" + matricula + ".jpg");
                Icon icon = new ImageIcon(wallper.getImage().getScaledInstance(200, 250, 200));

                user_image.setIcon(icon);

                insertarRegistroEnTabla(alumno.getNombre());

                enviarDatosArduino(alumno);
            }

        } catch (Exception e) {
        }


    }//GEN-LAST:event_jButton1ActionPerformed

